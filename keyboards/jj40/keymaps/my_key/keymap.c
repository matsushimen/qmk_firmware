bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _VIM);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _VIM);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _VIM);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _VIM);
      }
      return false;
      break;
    case VIM:
      if (record->event.pressed) {
        layer_on(_VIM);
        update_tri_layer(_VIM, _VIM, _VIM);
      } else {
        layer_off(_VIM);
        update_tri_layer(_LOWER, _RAISE, _VIM);
      }
      return false;
      break;
  }
  return true;
};
const macro_t *action_get_macro(keyrecord_t *record, uint8_t id, uint8_t opt) {
  if (record->event.pressed) {
    switch(id) {
      case 0:
        return MACRO(D(LALT), T(RIGHT), U(LALT), END);
      case 1:
        return MACRO(D(LALT), T(LEFT), U(LALT), END);
    }
  }
  return MACRO_NONE;
};
